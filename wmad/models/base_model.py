    def _inference(self):
        self.net1.eval()
        self.net2.eval()
        self.net3.eval()
        with torch.no_grad():
            z1_lst = []
            z2_lst = []
            z3_lst = []
            score_lst = []
            
            _iter_ = self.test_loader
            # if self.verbose >= 2:
            #     _iter_ = tqdm(self.test_loader, desc='testing: ')
            # else:
            #     _iter_ = self.test_loader

            for batch_data in _iter_:
                batch_z1, batch_z2, batch_z3, s = self.inference_forward(batch_data, self.net1, self.net2, self.net3, self.criterion)
                z1_lst.append(batch_z1)
                z2_lst.append(batch_z2)
                z3_lst.append(batch_z3)
                score_lst.append(s)

        z1 = torch.cat(z1_lst).data.cpu().numpy()
        z2 = torch.cat(z2_lst).data.cpu().numpy()
        z3 = torch.cat(z3_lst).data.cpu().numpy()
        scores = torch.cat(score_lst).data.cpu().numpy()

        return z1, z2, z3, scores

